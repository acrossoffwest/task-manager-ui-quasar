<template>
  <input type="text" v-model="dots" />
</template>

<script>
export default {
  name: "DotsLoader",
  data: () => ({
    dots: "",
    interval: null,
  }),
  methods: {
    dotsLoad() {
      this.interval = setInterval(() => {
        if (this.dots.length === 3) {
          this.dots = "";
        }

        this.dots += ".";
      }, 170);
    },
  },
  created() {
    this.dotsLoad();
  },
  destroyed() {
    clearInterval(this.interval);
  },
};
</script>

